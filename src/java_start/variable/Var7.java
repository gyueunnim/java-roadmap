package java_start.variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // true, false 가능
        char d = 'A'; // 문자 하나
        String e = "Hello Java"; // 문자열 -> 클래스 배우고 더 자세히 다룸

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        // 리터럴: 100, 10.5 처럼 내가 직접 적은 값들(고정된 값) -> 리터럴 값은 바꿀 수 없음. 코드를 바꾸는거지
    }
}
